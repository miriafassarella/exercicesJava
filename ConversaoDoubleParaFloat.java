public class ConversaoDoubleParaFloat {

    public static void main() {

	double peso1 = 20.5;
	
	float peso2 = (float) peso1;
	
	System.out.println(peso1);
	System.out.println(peso1);

	float taxa1 = 934.5f;
	double taxa2 = (double) taxa1;

	System.out.println(taxa1);
	System.out.println(peso2);

    }
}