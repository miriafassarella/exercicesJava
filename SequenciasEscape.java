public class SequenciasEscape {

    public static void main(String[] args) {


	System.out.println("Oi \"Miriã\"");//colocamos uma barra antes de cada apas que queremos que imprima.

	System.out.println("Seu nome : \nMiriã"); //barra + n é usado para quebrar linha.

	System.out.println("C:\\Users\\miria"); //usa-se duas barras para conseguir imprimir o endereço que contem barra.

    }



}